#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			break ;
		i++;
	}
	if ((s[i] && c) || (!s[i] && !c))
		return ((char *)(s + i));
	else
		return (NULL);
}
